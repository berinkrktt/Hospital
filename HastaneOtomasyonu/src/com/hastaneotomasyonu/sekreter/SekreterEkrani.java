    private void HastaTablosuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_HastaTablosuMouseClicked
        int selectedRow = HastaTablosu.getSelectedRow();
        
        String tc = model.getValueAt(selectedRow, 0).toString();
        TcAlani.setText(tc);
        String adSoyad = model.getValueAt(selectedRow, 1).toString();
        String[] dizi = adSoyad.split(" ");
        String ad;
        String soyad;
        switch (dizi.length) {
            case 4:
                ad = dizi[0] + " " + dizi[1];
                soyad = dizi[2] + " " + dizi[3];
                break;
            case 3:
                ad = dizi[0] + " " + dizi[1];
                soyad = dizi[2];
                break;
            default:
                ad = dizi[0];
                soyad = dizi[1];
                break;
        }
        AdAlani.setText(ad);
        SoyadAlani.setText(soyad);
        String ilac = model.getValueAt(selectedRow, 2).toString();
        IlacAlani.setText(ilac);
        

    }//GEN-LAST:event_HastaTablosuMouseClicked
     private void dinamikAra(String ara) {
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<DefaultTableModel>(model);

        HastaTablosu.setRowSorter(rowSorter);

        rowSorter.setRowFilter(RowFilter.regexFilter(ara));
    }
