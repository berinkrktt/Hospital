    private void TcAlaniKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TcAlaniKeyTyped
        char c = evt.getKeyChar();
        if (TcAlani.getText().length() >= 11 || (c == KeyEvent.VK_SPACE)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_TcAlaniKeyTyped

    private void EkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EkleActionPerformed
        String tc = TcAlani.getText();
        String ad = AdAlani.getText();
        String soyad = SoyadAlani.getText();
        String ilac = IlacAlani.getText();
        if (TcAlani.equals("") || AdAlani.equals("") || SoyadAlani.equals("") || IlacAlani.equals("")) {
            JOptionPane.showMessageDialog(this, "Bilgileri eksiksiz giriniz!!!");
        } else {
            int no = sekreterIslemleri.noOlustur();

            sekreterIslemleri.receteYaz(no, ad, soyad, ilac, tc);
            sonucGoruntule();
            receteNo = no;
        }
    }//GEN-LAST:event_EkleActionPerformed

