      public void sonucGoruntule() {
        model.setRowCount(0);
        ArrayList<Rect> sonuc = new ArrayList<>();
        sonuc = sekreterIslemleri.receteGoster();
        if (sonuc != null) {
            for (Rect sonc : sonuc) {
                int no = sonc.getNo();
                String tc = sonc.getTc();
                String adSoyad = sonc.getAd() + " " + sonc.getSoyad();
                String ilac = sonc.getIlacAd();
           
                
                Object[] ekleme = {tc, adSoyad, ilac};
                
                model.addRow(ekleme);
                
            }
        }
        
    }
